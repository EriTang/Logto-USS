"use strict";

import {prng_alea} from 'esm-seedrandom';

function getAvatar(seed, width, height, theme = 'default') {
  // Seed RNG.
  const rng = prng_alea(seed);

  let widthHeight = Math.max(width, height, 12);
  const canvas = document.createElement('canvas');
  canvas.width = widthHeight;
  canvas.height = widthHeight;

  // Avatar random parts.
  const parts = {
    legs: availableParts[theme].legs[Math.floor(rng() * availableParts[theme].legs.length)],
    hair: availableParts[theme].hair[Math.floor(rng() * availableParts[theme].hair.length)],
    arms: availableParts[theme].arms[Math.floor(rng() * availableParts[theme].arms.length)],
    body: availableParts[theme].body[Math.floor(rng() * availableParts[theme].body.length)],
    eyes: availableParts[theme].eyes[Math.floor(rng() * availableParts[theme].eyes.length)],
    mouth: availableParts[theme].mouth[Math.floor(rng() * availableParts[theme].mouth.length)]
  };

  const avatar = canvas.getContext('2d');
  avatar.fillStyle = generateRandomColor(rng);
  avatar.strokeStyle = avatar.fillStyle;
  avatar.lineWidth = 1;

  Object.values(parts).forEach(part => drawPart(part, avatar, canvas));

  return canvas.toDataURL();
}

function generateRandomColor(rng) {
  const gencol = () => Math.floor(rng() * 200) + 55;
  return `rgb(${gencol()}, ${gencol()}, ${gencol()})`;
}

function drawPart(part, avatar, canvas) {
  const dotSize = Math.floor(canvas.width / part.length);
  const offset = Math.round(0.5 * (canvas.width - dotSize * part.length));
  part.forEach((line, lineIndex) => {
    const y = lineIndex * dotSize + offset;
    line.forEach((col, colIndex) => {
      const x = colIndex * dotSize + offset;
      if (col === 1) {
        avatar.fillRect(x, y, dotSize, dotSize);
      } else if (col === 2) {
        avatar.clearRect(x, y, dotSize, dotSize);
      }
    });
  });
}

